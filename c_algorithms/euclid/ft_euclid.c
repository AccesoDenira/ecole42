int		ft_euclid(int i,int j)
{
	int		tmp;

	tmp = 0;
	while (i > 0)
	{
		if (i < j)
		{
			tmp = i;
			i = j;
			j = tmp;
		}
		i -= j;
	}
	return (j);
}
